# Função "len()".

#O len() retorna a quantidade de caracteres na string. Diferentemente das outras funções, ele é passado como um argumento da string:

string16 = "Meu nome é Geraldão."
print(string16) #Printa a sentença
print(len(string16)) #Printa o tamanho da sentença

print("O tamanho da String 16 é: '%s' caracteres." %len(string16)) #Deu certo, porra!

