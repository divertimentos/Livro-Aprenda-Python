#Center

string11 = "olá, meu nome é Geraldão de Rívia." #Sentença não capitalizada

print(string11) #Sentença sem alterações.

print(string11.center(50, "!").upper()) #Centralizando e colocando em caixa alta

print(string11.ljust(50, "*").upper()) #Texto à esquerda e em caixa alta..

print(string11.rjust(50, "@")) #Texto à direita e em caixa baixa().
